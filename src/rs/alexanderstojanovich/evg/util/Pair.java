package rs.alexanderstojanovich.evg.util;

/**
 *
 * @author Coa
 */
public class Pair<Key, Value> {

    private final Key key;
    private final Value value;

    public Pair(Key key, Value value) {
        this.key = key;
        this.value = value;
    }

    public Key getKey() {
        return key;
    }

    public Value getValue() {
        return value;
    }

}
